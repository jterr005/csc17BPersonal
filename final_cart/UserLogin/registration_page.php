		// Make the query:
		$q = "INSERT INTO cart_users (first_name, last_name, email, pass) VALUES ('$fn', '$ln', '$e', SHA1('$p') )";		
		$r = @mysqli_query ($dbc, $q); // Run the query.
		if ($r) { // If it ran OK.
		
			// Print a message:
			echo '<div style="text-align:center">
			<h1>Thank you, '; 
			echo $fn;
			echo '!</h1>
